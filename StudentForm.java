    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
    Student s = new Student();
    s.setFirstName(txtFirstName.getText());
    s.setLastName(txtLastName.getText());
    s.setAge(Integer.parseInt(txtAge.getText()));
    s.setEmail(txtEmail.getText());

    dao.addStudent(s); 
    refreshTable(); // This updates the UI immediately
    
    // Clear the fields for the next entry
    txtFirstName.setText("");
    txtLastName.setText("");
    txtAge.setText("");
    txtEmail.setText("");
    
    javax.swing.JOptionPane.showMessageDialog(this, "Student Added!");
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: Check your database connection or Age input.");
}
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      try {
    int id = Integer.parseInt(txtID.getText());
    dao.deleteStudent(id); 
    refreshTable();
    txtID.setText("");
    javax.swing.JOptionPane.showMessageDialog(this, "Student Deleted!");
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid ID to delete.");
}
    }//GEN-LAST:event_btnDeleteActionPerformed

