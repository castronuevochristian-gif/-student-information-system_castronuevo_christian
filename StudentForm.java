    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
    Student s = new Student();
    // It uses the ID from the text box to find the right person
    s.setStudentId(Integer.parseInt(txtID.getText())); 
    s.setFirstName(txtFirstName.getText());
    s.setLastName(txtLastName.getText());
    s.setAge(Integer.parseInt(txtAge.getText()));
    s.setEmail(txtEmail.getText());

    dao.updateStudent(s);
    refreshTable();
    javax.swing.JOptionPane.showMessageDialog(this, "Student Updated!");
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: Select a student first.");
}
    }//GEN-LAST:event_btnUpdateActionPerformed

